    public static Integer[] quickSort (Integer [] H, int begin, int end) {
        if (end > begin) {
            int j;
            j = divide(H, begin, end);
            quickSort(H, begin, j-1);
            quickSort(H, j+1, end);
        }

        return H;
    }
    public static Integer divide(Integer[]H, int begin, int end) {
        int Temp;
        int i = (begin-1);
        int pivot = H[end];
        
        for (int j = begin; j < end; j++) {
            if (H[j] <= pivot) {
                
            
                i++;
                
                Temp = H[i]; 
                H[i] = H[j]; 
                H[j] = Temp;
                        
            }
            }
        int temp = H[i+1]; 
        H[i+1] = H[end]; 
        H[end] = temp; 
  
        return i+1; 

     }
