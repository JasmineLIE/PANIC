    public static String Modifier (Integer A) {
        if (A == 1)
            return "-5";
        else if (A <= 3)
            return "-4";
        else if (A <= 5)
            return "-3";
        else if (A <= 7)
            return "-2";
        else if (A <= 9)
            return "-1";
        else if (A <= 11)
            return "0";
        else if (A <= 13)
            return "1";
        else if (A <= 15)
            return "2";
        else if (A <= 17)
            return "3"; 
        else if (A <= 19)
            return "4";
        else if (A <= 21)
            return "5";
        else if (A <= 23)
            return "6";
        else if (A <= 25)
            return "7";
        else if (A <= 27)
            return "8";
        else if (A <= 29)
            return "9";
        else if (A >= 30)
            return "10";
        
        
     return null;   
    }
    
